    def __init__(self, img_path):
        self.__im = Image.open(img_path)
        # 保存切割后的图像信息
        self.__fg_color = (0, 0, 0)
        self.__bg_color = (255, 255, 255)
        self.__im_list = []
        self.__yzm = ''
        self.__im2bw()
        # 图像切割
        self.__cutting()
        # set验证码数字
        self.__setvalues()

    def __im2bw(self):
        '''
        图像二值化
        '''
        w, h = self.__im.size
        pix = self.__im.load()
        # 二值化
        for x in range(w):
            for y in range(h):
                if pix[x, y] == self.__fg_color:
                    continue
                pix[x, y] = self.__bg_color

    def __getcutpix(self, direc=0):
        '''
        获取图像有效区域的坐标
        direc 扫描的方向：
        direc = 0 or 1,
        0:横向扫描
        1：纵向扫描
        '''
        w, h = self.__im.size
        pix = self.__im.load()
        # 图像切割
        end_list = []
        start_list = []
        # 横向扫描
        if direc == 0:
            # 前一列上是否有黑点
            pre_line = False
            for x in range(w):
                # 当前扫描的列上是否有黑点
                cur_line = False
                for y in range(h):
                    if pix[x, y] == self.__fg_color:
                        cur_line = True
                        break
                # 若当前列存在黑点,且前一列不存在黑点时,则当前的x为小长方形的横向起始坐标
                if (cur_line and not pre_line):
                    start_list.append(x)
                # 若当前列不存在黑点,前一列存在黑点,则当前列的x坐标为小长方形的横向结束坐标
                elif (not cur_line and pre_line):
                    end_list.append(x)
                pre_line = cur_line
        # 纵向扫描
        elif direc == 1:
            # 前一行上是否有黑点
            pre_line = False
            for x in range(h):
                # 当前扫描的行上是否有黑点
                cur_line = False
                for y in range(w):
                    if pix[y, x] == self.__fg_color:
                        cur_line = True
                        break
                # 若当前行存在黑点,且前一行不存在黑点时,则当前的x为小长方形的纵向起始坐标
                if (cur_line and not pre_line):
                    start_list.append(x)
                # 若当前行不存在黑点,前一行存在黑点,则当前列的x坐标为小长方形的纵向结束坐标
                elif (not cur_line and pre_line):
                    end_list.append(x)
                pre_line = cur_line
        result_list = [(i, j) for (i, j) in zip(start_list, end_list)]
        return result_list

