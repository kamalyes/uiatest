    def __denosing(image):
        '''
        对图像去噪
        '''
        im = image
        w, h = im.size
        for x in range(w):
            for y in range(h):
                # 若是白点，则不处理
                if im.getpixel((x, y)) == 255:
                    continue
                # 图像四边的点全填充为白色
                if x == 0 or y == 0 or x == w - 1 or y == h - 1:
                    im.putpixel((x, y), 255)
                    continue
                # 记录当前点周围8个点中黑点数
                black_point = 0
                if im.getpixel((x - 1, y - 1)) == 0:
                    black_point = black_point + 1
                if im.getpixel((x, y - 1)) == 0:
                    black_point = black_point + 1
                if im.getpixel((x + 1, y - 1)) == 0:
                    black_point = black_point + 1
                if im.getpixel((x - 1, y)) == 0:
                    black_point = black_point + 1
                if im.getpixel((x + 1, y)) == 0:
                    black_point = black_point + 1
                if im.getpixel((x - 1, y + 1)) == 0:
                    black_point = black_point + 1
                if im.getpixel((x, y + 1)) == 0:
                    black_point = black_point + 1
                if im.getpixel((x + 1, y + 1)) == 0:
                    black_point = black_point + 1
                # 若周围的黑点数小于3个，则认为当前点应该为白点
                if black_point <= 2:
                    im.putpixel((x, y), 255)
